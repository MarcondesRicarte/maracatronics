public class Ball {

	// public static double CHARGE = -1;
	
	private float x;
	private float y;
	
	
	public Ball(){
		this.x = 0;
		this.y = 0;
	}


	public float getX() {
		return x;
	}


	public void setX(float x) {
		this.x = x;
	}


	public float getY() {
		return y;
	}


	public void setY(float y) {
		this.y = y;
	}
	
	
}
